const dropdowns = document.querySelectorAll(".dropdown");

dropdowns.forEach((dropdown) => {
  const select = dropdown.querySelector(".select");
  const caret = dropdown.querySelector(".caret");
  // const dmenu = dropdown.querySelector(".dmenu");
  const dmenu = dropdown.querySelector(".con");
  const options = dropdown.querySelectorAll(".dmenu li");
  const selected = dropdown.querySelector(".selected");

  select.addEventListener("click", () => {
    selected.classList.toggle("select-clicked");
    caret.classList.toggle("caret-rotate");
    // dmenu.classList.toggle('dmenu-open');
    dmenu.classList.toggle("con-open");
  });
  options.forEach((option) => {
    option.addEventListener("click", () => {
      // selected.innerHTML = option.innerHTML;
      selected.classList.remove("select-checked");
      caret.classList.remove("caret-rotate");
      // dmenu.classList.remove("dmenu-open");
      options.forEach((option) => {
        option.classList.remove("active");
      });
      option.classList.add("active");
    });
  });
});

